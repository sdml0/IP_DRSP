uint32_t parse_POST(uint8_t **req)
{
	uint8_t* ptr = *req;
	uint32_t len = 0;

	while ((*(uint32_t*)ptr != 0x746E6F43) || (*(uint32_t*)(ptr + 4) != 0x2D746E65) ||
	(*(uint32_t*)(ptr + 8) != 0x676E654C) || (*(uint32_t*)(ptr + 12) != 0x203A6874)) ptr++; 		//	"Content-Length: "
			
		ptr += 16;
		while ((*ptr <= 0x39) && (*ptr >= 0x30)) {
			len = 10*len + *ptr - 0x30;
			ptr++;
		}
	while (*(uint32_t*)ptr != 0x0A0D0A0D) ptr++;
		
	*req = ptr + 4;
	return len;
	
	
}

uint32_t let2int (uint32_t* str)
{	
	uint32_t t0, t1;
	
	t0 = str[0] - 0x61616161;			// little endian sent - rev view
	t0 = (t0>>4) | t0;
	t0 = ((t0>>8)&0xFF00) | (t0&0xFF);
	t1 = str[1] - 0x61616161;
	t1 = (t1>>4) | t1;
	t1 = ((t1<<8)&0xFF000000) | ((t1<<16)&0xFF0000);
		
	return t1 | t0;
}

uint32_t hex2int(uint8_t* str)
{
	uint32_t res = 0;
	uint8_t c;
	
	while (1) {
		c = *str;
		if ((c <= 0x39) && (c >= 0x30)) c -= 0x30;
		else if ((c <= 0x41) && (c >= 0x46)) c -= 0x41 - 0xA;
		else if ((c <= 0x61) && (c >= 0x66)) c -= 0x61 - 0xA;
		else break;
		res = (res<<4) | c;
		str++;		
	}

	return res;
}

uint32_t dec2int(uint8_t* str)
{
	uint32_t res = 0;
	uint8_t c;
	
	while (1) {
		c = *str;
		if ((c <= 0x39) && (c >= 0x30)) c -= 0x30;
		else break;
		res = 10*res + c;
		str++;		
	}

	return res;
}

uint16_t byte2hex(uint8_t n)
{
	uint16_t res;
	
	if ((n & 0xF) < 10) res = ('0' + (n & 0xF));
	else res = ('A' + (n & 0xF) - 10);
	n >>= 4;
	res <<= 8;
	if (n < 10) res |= '0' + n;
	else res |= 'A' + n - 10;

	return res;
}

void IntToStr(uint8_t* endOfBuf, uint32_t n)
{
	uint32_t m;
		
	while(n) {
		endOfBuf--;
		m = n;
		n /= 10;
		*endOfBuf = m - 10*n + '0';
	}
}

